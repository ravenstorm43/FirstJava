package week02;

public class W11 {
    public static void main(String[] args) {
        boolean flag = true;
        if(flag) {
            System.out.println("값이 true입니다.");
        } else {
            System.out.println("값이 false입니다.");
        }

        int number = 2;

        if (number == 1) {
            System.out.println("number값은 1입니다.");
        } else if (number == 2) {
            System.out.println("number값은 2입니다.");
        } else {
            System.out.println("number값은 모르는 값입니다.");
        }
    }
}
